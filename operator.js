// Equal Operator (==)
let equalOperator = 100
console.log("equalOperator")
console.log(equalOperator == 100) // true
console.log(equalOperator == 20) // false

// Not Equal ( != )
var notEqual = "rajin"
console.log("notEqual")
console.log(notEqual != "malas") // true
console.log(notEqual != "pandai") //true 